                        <div class="panel-body">
                         <p>The Society’s primary focus has been to promote the adoption of best reading practices across Africa. In line with this, the Society organized a book drive campaign to address the lack of educational materials in Government school libraries. This drive was an initiative to promote a reading culture among children and youth.</p>

                         <p>Through this campaign, the Society received reading materials with the support of individuals who donated towards the project. The books received included; educational text books cutting across all subjects like Mathematics, English Language, Government, Economics, Basic Science, Biology etc. Among notable donations were Classic African Literature novels from our finest authors <i>(Things Fall Apart/Arrow of god by Chinua Achebe, The gods are Not to Blame by Ola Rotimi, The African Child by Camara Laye etc.)</i>, Non-African Classic Literature novels <i>(Twelfth Night by Williams Shakespeare, Oliver Twist by Charles Dickens etc)</i> to say the least. </p>

                         <p>The Society’s team of volunteers visited Ifako Comprehensive Senior High School, Ifako-Gbagada and Girls Junior Secondary Grammar School, Ikoyi on Friday, October 21, 2016 to give the gift of reading to students and teachers of these schools by donating the reading materials received to the schools’ libraries.</p>

                         <p>The Society’s team of volunteers advised the students on why it is important to develop the rudiments of reading and the culture of reading always so as to survive in life as it adds quality to life and provides access to culture and cultural heritage. Ms. Arogundade added that reading experience can be obtained in the library. She also remarked that the school library is a gateway to knowledge and will serve as a starting point or road map for reading and the promotion of a reading culture.</p>

                         <p><img class="why-choose-img" src="assets/images/news book 2.png" alt="img"></p>

                         <br><br><br><p>Mr. Kunle Okunowo, Principal, Ifako Comprehensive Senior High School, Ifako-Gbagada appreciated the Society for its effort in the promotion and development of a reading culture in public schools and also for executing this laudable project to support the work of government in the country. Miss Otunoye Chioma Sarah in S.S.S 3 gave the vote of thanks on behalf of all the students of Ifako Comprehensive Senior High School and promised to encourage her fellow students to visit the library more often as reading is essential for lifelong learning.</p>

                         <p>The school librarian of Girls Junior Secondary Grammar School, Ikoyi, Mr. Akande, thanked the Society for its keen interest in offering learning support to government schools by providing reading materials, books and other instructional materials. The Library Prefect, Miss Houessou Iris also thanked the Society for the generous donation to their school library and noted that this will enable her and other students spend more time in the library.</p>

                         <p>This event was supported by Dr. Enase Okonedo, Ms. Oludolapo Makinde, Ms. Debora Nkor, Mrs. Gagar, Ms. Arogundade, Mrs Dunni Akinrele, Ms. Blessing Sunday, Mrs. Shamsiya Sadiq-Mohammed and Mrs. Adedoyin-Odunfa.</p>

