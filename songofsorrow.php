                      <p><b><h3>ANALYSIS OF THE POEM</h3></b></p>
                      
                    
                      <p>The poem, Songs of Sorrow is drawn from the laments of a man besieged by his own misfortune and inevitable fate. The author, George Kofi Nyidevu Awoonor-Williams who hails from the Gold-coast, present-day Ghana was able to depict poetic traditions of his native <i>Ewe</i>Â people which heavily influenced his writing.</p>

                      <p>The poem portrays a man in despair that fervently ascribes his misfortune to his ancestors and the reason for his assertion is not farfetched.  There is in African philosophy, an understanding that death in itself is a channel by which the departed become ancestral spirit in the life hereafter, with a responsibility to cater for the living.</p>


                      <p>This perception has largely nurtured a belief that men who have come about great success and fortune must have been aided by ancestral spirits. This view is also portrayed in Chinua Achebeâs âThings Fall Apartâ when the protagonist, Okonkwo was admonished by an elder at a kindred meeting â <i>those whose palm-kernels were cracked for them by a benevolent spirit should not forget to be humble.</i> It is in this unique understanding and distinctiveness that Awoonor delivers this poem. Hence the opening;</p><br>

                      <p><i>âDzogbese Lisa has treated me thusâ<br>
                      It has led me along the sharps of the forest...â</i></p><br>

                      <p>Awoonor portray the man to be of great helplessness at the circumstances surrounding his present condition as though his personal effort were incapable of changing his situation. A deeper understanding of the myth behind the poem may suggest that the author was perhaps explaining the occurrence of death from a philosophical point of view;</p><br>

                      <p><i>...Returning is not possible and going forward is a great difficulty.â</i></p><br>

                      <p>The poem has in its narrative a lamentation of suffering, however, the author also conveys a proverbial message that suffering in itself is a part of life, an aspect of human endeavour that one must brazen out.</p><br>

                      <p><i>âThe affairs of this world are like the chameleon feces<br>
                        Into which I have stepped<br>
                        When I clean it cannot goâ</i></p><br>

                      <p>An aspect of our social construct is also drawn out of this work; and the author cleverly portrayed the man complaining bitterly about his social standing in his immediate environment.</p><br>

                      <p><i>I am on the worldâs extreme corner,<br>
                      I am not sitting in the row with the eminent<br>
                      But those who are lucky<br>
                      Sit in the middle and forget<br>
                      I am on the worldâs extreme corner<br>
                      I can only go beyond and forget</i></p><br>

                      <p>He identifies those who sit in the front row as being âthe eminentâ, the well-to-do, who live their lives in stupendous wealth. The next class of persons being the âmiddle classâ, he portrays them, in my view, to be deluded by the respite of comfort, as they cling to the eminent who have abundant wealth. <i>â...but those who are lucky sit in the middle and forgetâ. </i></p>

                      <p>The writer in this poem deliberately or inadvertently amplifies the disparity between the rich and the poor. There is a great divide in the class struggle and this perhaps why the man in the poem is so conscious of his state. He is reminded daily of his predicament, and he laments; that only death may allay him of the suffering:</p><br>

                      <p><i>â...I can only go beyond and forgetâ.</i></p><br>

                      <p>The authorâs language in the poem carries the strong harmony of his native <i>Ewe tongue</i> into English. <i>Ewe</i> is a highly tonal language, sung as much as spoken, with tonality determining the meaning of innumerable words. It would not be out of place to describe the poem as a song of laments. </p><br>

                      <p><i>âMy people, I have been somewhere<br>
                      If I turn here, the rain beats me<br>
                      If I turn there the sun burns meâ</i></p><br>

                      <p>Awoonor also extols those who have been able to break even in life, he recognised that success was difficult to attain and fortune, hard to gather. Awoonor expresses that wealth would only accrue to those who are resolute;</p><br>

                      <p><i>âThe firewood of this world <br>
                      Is for only those who can take heart<br>
                      That is why not all can gather it.'</i></p><br>

                      <p>Again, the man in the poem takes solace in the stoic belief of a divine fate. He excuses his indolence with the idea that his ancestors allowed this fate (his predicament) befall him.</p><br>

                      <p><i>'...the world is not good for anybody<br>
                      But you are so happy with your fate<br>
                      Alas! The travellers are back<br>
                      All covered with debtâ</i></p><br>

                      <p>The second part of the poem portrays the man reflecting on the outcome of his life. His grief is profound as he reels out his catalogue of woes. The poem depicts the man to be in the evening of his life (elderly) and he appears to be the sole surviving heir from a linage of great men. By this poem, Awoonor also alludes to the myth surrounding childlessness in African culture and the embarrassment that accompanies it.</p><br>

                      <p><i>'Something has happened to me<br>
                      The thing so great that I cannot weep;<br>
                      I have no sons to fire the gun when I die<br>
                      And no daughters to wail when I close my mouth<br>
                      I have wandered on the wilderness<br>
                      The great wilderness men call life<br>
                      The rain has beaten me,<br>
                      And the sharp stumps cut as keen as knives<br>
                      I shall go beyond and rest.<br>
                      I have no kin and no brother,<br>
                      Death has made war upon our houseâ</i></p><br>

                      <p>The poem closely with the news of the demise of Agosi, presumably, his closest associate with whom he shouldered his grief and with this singular occurrence he completely resigns to fate;</p><br>

                      <p><i>âSomebody is dead. Agosi himself<br>
                      Alas! A snake has bitten me<br>
                      My right arm is broken,<br>
                      And the tree on which I lean is fallenâ</i></p><br>

                      <p>The poem ends with a charge to Agosi, to convey their displeasure to the ancestors; as though to admonish the gods;</p><br>

                      <p><i>âAgosi if you go tell them,<br>
                      Tell Nyidevu, Kpeti, and Kove<br>
                      That they have done us evil...;<br>
                      Ask them why they idle there<br>
                      While we suffer, and eat sand.<br>
                      And the crow and the vulture<br>
                      Hover always above our broken fences<br>
                      And strangers walk over our portion.â</i></p><br>

                      <p>Awoonor delivers the poem with great dexterity and mastery; he is acclaimed to have written drafts in <i>Ewe</i> before producing both Ewe and English final versions. Awoonor met his untimely death on September 21, 2013, he was among those killed in an attack at the Westgate shopping mall in Nairobi.</p>



