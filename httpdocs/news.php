					<p>The issue of a dwindling reading culture among the youth came to the fore recently at the 2017 Little Writers Awards organised by ALFA Reading Society for Junior and Senior Secondary Schools in Lagos. The yearly event, held at Oriental Hotel, Lagos, was another bold effort by the society, a not-for-profit organisation committed to the promotion of reading culture and the attainment of sustainable educational development across Africa.</p>
					
					<p>President of the society and Dean, Lagos Business School, Dr. Enase Okonedo, said the event, opened to junior and senior secondary schools in Lagos, was the high point of activities organised yearly to address the declining reading culture. Stressing that poverty has denied many students access to reading materials, she urged the youth to go back to reading to develop and broaden their minds for development.</p>

					<p>According to her, recent study has shown that 40 per cent of Nigeria graduates do not read after school. Okonedo expressed happiness with the acceptance of the yearly Little Writers’ Awards, with the increasing number of schools participating, thereby promoting reading.<p>
					<p>The society, she said, also embarked on a yearly book drive to encourage reading among pupils. Expounding on the theme ‘Reviving the Reading Culture: A Prerequisite for Self and National Development,’ Principal Consultant of Discover Your Potential Ltd, Dr. Kunbi Wuraola, said when one cannot read, he cannot write well and if one don’t write well, one cannot speak well. She stressed that everyone has a role to play to revive reading culture in the society.</p>

