                      <h2><b>THINGS FALL APART </b></h2>
                      <p>By CHINUA ACHEBE</p>
                    </div>
                    <div class="blog-news-details blog-single-details">
                      <p>Things Fall Apart is a classic piece of African Literature that has captured the hearts of many readers. It is in fact a staple book in many African schools and is regarded as one of the first African novels written in English to receive global acclaim. One interesting point about the book is the derivation of the title from W.B Yeats poem, “The Second Coming”. The text aptly portrays the culture and traditions of the Igbo Society as well as the effect of the emergence of British colonialism and Christian missionaries on the traditional African society.</p>

                      <p>The background of the novel is set in the pre-colonial Nigeria 19th century and follows the life of a man named Okonkwo. Achebe portrays Okonkwo as a determined and self-motivated person whose inherent goal in life is to be better than his father, Unoka, whom he regards as lazy and wasteful. Okonkwo strives to be wealthy, influential and powerful and succeeds in that regard.</p>

                      <p>Okonkwo as the protagonist of the novel is given to shows of fame and power. It is apparent that his greatest fear is being considered weak. It is for this reason that despite the apparent admiration and love he had for Ikemefuna, he discards the advice of Ezeudu and is the first one to strike Ikemefuna with his machete.</p>

