          <div class="panel panel-default">
                      <div class="panel-heading">
                        <h4 class="panel-title">
                          <a data-toggle="collapse" data-parent="#accordion" href="#collapseTwo">
                            AIFA Reading Society ‘Calculate2Succeed’ Project <span class="fa fa-minu-square"></span>
                            
                          </a>
                        </h4>
                      </div>
                      <div id="collapseTwo" class="panel-collapse collapse in">
                        <div class="panel-body">
                        
                         <p>In the Society’s effort to promote a sustainable framework for the development of education in Nigeria, the Society undertook its project tagged ‘Calculate2Succeed’ which is geared towards empowering students in public schools with the tools necessary to excel in mathematics and science subjects.</p>

                         <p>The importance of excelling in the field of mathematics cannot be overemphasized in our technologically driven world. This is more so in view of the fact that Mathematics is a compulsory subject offered in Nigerian schools and taught daily in all schools at least four times in a week. The benefits of a sound understanding of mathematics for students include the opportunity to develop lifelong learning skills like logical thinking, organization, problem solving capabilities and good work ethics which will ultimately have a positive impact on their career plan.</p>

                         <p>Representatives and a member of the Society, Ms. Oludolapo Makinde visited four (4) public schools in Lagos state with instructional materials to motivate and sustain their interest in mathematics learning as well as facilitate their understanding of the subject. The ‘Calculate2Succeed’ project was supported by Mrs. Chioma Mordi and through this project the Society was able to impact one thousand one hundred and eighty (1180) students in Lagos state with branded mathematical sets. The beneficiaries of this project include; Ifako Comprehensive Junior Grammar School, Lafiaji Comprehensive Senior Secondary School, Ojoku Memorial Junior High School, and Surulere Girls Senior Secondary School.</p>

                         <p>It is the Society’s hope that this project will help foster excellence in public secondary schools in Nigeria by supporting the students with basic instructional materials needed to help them succeed and reach their full potentials.</p>

                         

