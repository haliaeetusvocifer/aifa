                        <div class="panel-body">
                        
                         <p>In its continuous drive towards promoting reading culture in Nigeria and Africa, AIFA Reading Society has rewarded outstanding teachers and students in junior and senior secondary schools in Lagos State. At its 2017 Little Writers Award held recently, at the Oriental Hotel, Victoria Island, Lagos, with the theme: ‘Reviving the Reading Culture: A Prerequisite for self and National Development’, three winners from each of the three categories emerged and were rewarded with lofty cash prizes and other items.</p>
                         
                         <p>In the Teachers category, Mr. Ajani Oluyemi of Girls’ Junior Academy, Lagos Island, emerged the star prize winner, and was rewarded with the sum of N150, 000, while Ms Onaneye Salewa of Lafiaji Senior High School, Lagos Island, and Mr. Martins Odiagbe of Magbon-Alade Senior Grammar School, who were first and second runners up went home with the sum of N100, 000, and N50,000 respectively. In the Senior Secondary category, Miss Chidinma Okonkwo of Okesuna Senior High School, Lagos Island, won the star prize award and was handed the sum of N100, 000. The runners up, Miss Alfred Ini-Obong of Magbon-Alade Senior Grammar School and Miss Anani Peace Faustina of Ajeromi Ifelodun Senior High School, got the sum of N50,000, and N25, 000 respectfully.</p>
                         
                         <p>Also, in the junior secondary category, Miss Ibiyemi Wuraola Hafsat of Magbon-Alade Junior Grammar School emerged the star prize winner and was rewarded with the sum of N50, 000. First and second runners-up, Miss Bejide Tunmise, also of Magbon-Alade Junior Grammar School, and Miss Marry-Ann Eze of Ajeromi Ifelodun High School, got cash rewards of N25,000 and N15, 000 respectively. In addition to the cash rewards, each of the winners in all the categories received a carton of Cheri Noddle, Bliss Biscuits, Blue Boat Powder Milk from Olam International and a School bag from ACCION Microfinance Bank.</p>
                         
                         <p>Speaking at the event, President, AIFA Reading Society and Dean, Lagos Business School, Dr. Erase Okonedo, said people should go back to reading to broaden their minds in order to develop the economy of the nation and continent at large. She noted that because of the high level of poverty particularly in the rural areas, people had no access to reading materials. She explained that the Little Writers Award organised every year by the society was intended to rouse the interest of the students in reading and competition.</p>
                         
                         <p>In her appreciation to the society for the award, the star prize winner in the senior secondary category, Miss Chidimma Okonkwo, said: “I feel so happy and overwhelmed and I’m grateful to God for enabling me to win this award. I want to say a big thank you to AIFA Reading Society because this platform has really boosted my confidence in writing and speaking. They should keep doing their great work for our society.”</p>
							
