                      <div id="collapseFour" class="panel-collapse collapse">
                        <div class="panel-body">
                         <p>The AIFA Reading Society held its <b><i>‘Little Writers Awards’</i></b> ceremony on Friday, October 7, 2016 at Adada House, Old G.R.A, Nsukka, Enugu State.</p>

                         <p>The event was held under the distinguished chairmanship of Nestor Ezeme, Esq., Chairman, Post Primary School Management Board (PPSMB), Enugu. The occasion was well attended with over 200 guests comparing of students, teachers, directors of various educational boards, traditional rulers of various communities amongst others.</p>

                         <p>The Director of the Society/Principal, Kenna Partners, Professor Fabian Ajogwu, SAN, in his welcome remarks appreciated all attendees at the event and encouraged everyone to imbibe the culture of reading at all levels.</p>

                         <p>The keynote address was delivered by Professor Patrick Ngwu of the University of Nigeria, Nsukka (UNN) on <b><i>“The Importance of Reading Culture in the Society”.</i></b></p>

                         <p>The event marked the maiden edition of the AIFA Reading Society Annual Essay Competition for Junior and Senior Schools Categories in the eastern region of the Country.</p>

                         <p>For Junior School category, Ogbobe Maria Goretti of Community Secondary School, Abbi/Ugbene won the 1st prize of N50,000 while Ogbobe Monica of Community Secondary School, Abbi/Ugbene grabbed the 2nd price and Ugwu David of Community Secondary School, Nimbo took the 3rd price respectively. In the Senior School Category, Ajogwu Felicia Chinenye of Community Secondary School, Abbi/Ugbene won the 1st prize of N100, 000 while Agu Michael and Uburozo Alphonsus of Community Secondary School won the 2nd and 3rd prize respectively. Mr. Emeka Lazarus of Community Secondary School, Abbi/Ugbene won the 1st prize of N150, 000 in the teachers’ category while Mrs. Beatrice Chinwe Eze of Community Secondary School, Nimbo took the 2nd prize and Mr. Johnson Uche Eze of Community Secondary School, Abbi/Ugbene bagged the 3rd prize.</p>

                         <p>The event also featured poetic recitations by students and two stage plays on <b><i>“The Importance of Learning”</i></b> and <b><i>“Curbing Cultism in Nigerian Schools”</i>.</p></b>

                         <p>The beneficiaries of the awards were appreciative of the effort of the Society for its keen interest in encouraging and imbibing of excellent reading culture and practices in their State.</p>

                         <p>AIFA Reading Society | October 10, 2016</p>

